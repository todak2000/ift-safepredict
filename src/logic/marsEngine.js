/**
 * MARS hinge functions and prediction equations.
 * All inputs MUST be scaled to [-1, 1] using scalers.js before calling these.
 *
 * Notation:
 *   hp(x, k) = max(0, x - k)   [right hinge]
 *   hm(x, k) = max(0, k - x)   [left hinge]
 */

